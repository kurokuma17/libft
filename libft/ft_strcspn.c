#include "libft.h"

size_t	ft_strcspn(const char *s, const char *charset)
{
	size_t	i;
	size_t	j;

	i = 0;
	while (s[i])
	{
		j = 0;
		while (charset[j])
		{
			if (charset[j] == s[i])
				return (i);
			j++;
		}
		i++;
	}
	return (i);
}
